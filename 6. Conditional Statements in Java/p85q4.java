//WAP to get a number from user and print whether its divisible by 5 or not
class p85q4
{
    public static void main(int n)
    {
        if(n%5 == 0)
        {
            System.out.println(n+" is divisible by 5");
        }
        else
        {
            System.out.println(n+" is not divisible by 5");
        }
    }
}